a_side1 = int(input("Enter the length of side1 of a triangle:" ));
b_side2 = int(input("Enter the length of side2 of a triangle:" ));
c_side3 = int(input("Enter the length of side3 of a triangle" ));
perimeter = a_side1 + b_side2 + c_side3;
print("Perimeter of a triangle = ", perimeter);